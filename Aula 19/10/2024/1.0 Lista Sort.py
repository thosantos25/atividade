# O Sort Coloca a lista automatica em crescente.
lista = [1, 3, 12, 8, 2]
lista.sort()
print(lista)

# Coloca a lista em ordem decrecente pelo True.
lista.sort(reverse = True )
print(lista)

# Coloca a lista em ordem crescent e pelo False.
lista.sort(reverse = False)
print(lista)

